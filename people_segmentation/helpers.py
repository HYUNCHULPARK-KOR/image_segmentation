def get_colors(N, bright=True):
    brightness = 1.0 if bright else 0.7
    hsv = [(i / N, 1, brightness) for i in range(N)]
    colors = list(map(lambda c: colorsys.hsv_to_rgb(*c), hsv))
    return colors


def get_image_with_mask(image, boxes, masks, class_ids, single_color=False):
    N = boxes.shape[0]
    if not N:
        print("\n*** No instances to display *** \n")
    else:
        assert boxes.shape[0] == masks.shape[-1] == class_ids.shape[0]

    colors = get_colors(N)

    masked_image = image.astype(np.uint32).copy()
    for i in range(N):
        color = colors[0] if single_color else colors[i]

        # Bounding box
        if not np.any(boxes[i]):
            # Skip this instance. Has no bbox. Likely lost in image cropping.
            continue

        # Mask
        mask = masks[:, :, i]
        for c in range(3):
            masked_image[:, :, c] = np.where(mask == 1,
                                             image[:, :, c] *
                                             (1 - 0.5) + 0.5 * color[c] * 255,
                                             masked_image[:, :, c])
    return masked_image.astype(np.uint8)


def save_image(image_path, detect_func):
    image = skimage.io.imread(image_path)
    results = detect_func(image)
    r = results[0]
    masked_image = get_image_with_mask(image, r['rois'], r['masks'], r['class_ids'])
    skimage.io.imsave("people_{:%Y%m%dT%H%M%S}.png".format(datetime.datetime.now()), masked_image)


def save_video(video_path, detect_func):
    import cv2

    # Video capture
    vcapture = cv2.VideoCapture(video_path)
    width = int(vcapture.get(cv2.CAP_PROP_FRAME_WIDTH))
    height = int(vcapture.get(cv2.CAP_PROP_FRAME_HEIGHT))
    fps = vcapture.get(cv2.CAP_PROP_FPS)

    # Define codec and create video writer
    file_name = "people_{:%Y%m%dT%H%M%S}.avi".format(datetime.datetime.now())
    vwriter = cv2.VideoWriter(file_name,
                              cv2.VideoWriter_fourcc(*'MJPG'),
                              fps, (width, height))
    count = 0
    success = True
    while success:
        print("frame: ", count)
        # Read next image
        success, image = vcapture.read()
        if success:
            # OpenCV returns images as BGR, convert to RGB
            image = image[..., ::-1]
            # Detect objects
            r = detect_func(image)
            # Color splash
            masked_image = get_image_with_mask(image, r['rois'], r['masks'], r['class_ids'], single_color=True)
            # RGB -> BGR to save image to video
            masked_image = masked_image[..., ::-1]
            # Add image to video writer
            vwriter.write(masked_image)
            count += 1
    vwriter.release()
    print("Saved to ", file_name)


